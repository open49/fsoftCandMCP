#include <stdio.h>
#include <stdint.h>

/******************************************************************************
*MACRO
******************************************************************************/

#define REG_BIT_SET32(add, n) ((*(add)) |= (1 << (n)))
#define REG_BIT_CLEAR32(add, n) ((*(add)) &= ~(1 << (n)))
#define SWAP_BYTE32(add) ((*(add)) = (((*(add)) & 0xFF000000) >> 24) | \
                                     (((*(add)) & 0x00FF0000) >> 8)  | \
                                     (((*(add)) & 0x0000FF00) << 8)  | \
                                     (((*(add)) & 0x000000FF) << 24))

#define SWAP_BYTE16(add) (*(add) = (((*(add)) & 0x00FF) << 8) | \
                                   (((*(add)) & 0xFF00) >> 8))

/******************************************************************************
*MAIN
******************************************************************************/
int main() {
    uint32_t a = 0x12345678;
    printf("Before SWAP_BYTE32: 0x%08X\n", a);
    SWAP_BYTE32(&a);
    printf("After SWAP_BYTE32:  0x%08X\n", a);

    uint16_t b = 0x1234;
    printf("Before SWAP_BYTE16: 0x%04X\n", b);
    SWAP_BYTE16(&b);
    printf("After SWAP_BYTE16:  0x%04X\n", b);

    uint32_t c = 0;
    printf("Before REG_BIT_SET32: %u\n", c);
    REG_BIT_SET32(&c, 2);
    printf("After REG_BIT_SET32:  %u\n", c);

    return 0;
}
